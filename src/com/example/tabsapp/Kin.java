package com.example.tabsapp;

public class Kin {
	private String wordLg1;
	private String wordLg2;
	
	public Kin(String wordLg1, String wordLg2) {
		super();
		this.wordLg1 = wordLg1;
		this.wordLg2 = wordLg2;
	}

	public String getWordLg1() {
		return wordLg1;
	}
	public void setWordLg1(String wordLg1) {
		this.wordLg1 = wordLg1;
	}
	public String getWordLg2() {
		return wordLg2;
	}
	public void setWordLg2(String wordLg2) {
		this.wordLg2 = wordLg2;
	}
}
